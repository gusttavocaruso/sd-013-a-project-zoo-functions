function countAnimals(animal) {
  if (!animal) {
    const allAnimals = {};
    species.forEach(({ name, residents }) => {
      allAnimals[name] = residents.length;
    });
    return allAnimals;
  }
  return species.find((specie) =>
    specie.name === animal).residents.length;
}

